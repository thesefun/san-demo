								<p class="text-justify">
									<strong>Cream</strong> is a dairy product composed of the higher-butterfat layer skimmed from the top of milk before 
									homogenization. In un-homogenized milk, the fat, which is less dense, will eventually rise to the top. In the industrial 
									production of cream, this process is accelerated by using centrifuges called "separators". In many countries, it is sold 
									in several grades depending on the total butterfat content. It can be dried to a powder for shipment to distant markets, 
									and is known to contain high levels of saturated fat.
								</p>
								<p class="text-justify">
									Cream skimmed from milk may be called "sweet cream" to distinguish it from cream skimmed from whey, a by-product of 
									cheese-making. Whey cream has a lower fat content and tastes more salty, tangy and "cheesy". In many countries, 
									cream is usually sold partially fermented: sour cream, crème fraîche, and so on. Both forms have many culinary uses in 
									sweet, bitter, salty and tangy dishes.
								</p>
								<p class="text-justify">
									Cream produced by cattle (particularly Jersey cattle) grazing on natural pasture often contains some natural carotenoid 
									pigments derived from the plants they eat; this gives it a slightly yellow tone, hence the name of the yellowish-white 
									color: cream. This is also the origin of butter's yellow color. Cream from goat's milk, or from cows fed indoors on grain 
									or grain-based pellets, is white.
